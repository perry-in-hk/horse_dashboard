/**
 * Convert DD/MM/YYYY to YYYY/MM/DD for HKJC local URL query param.
 */
export function toLocalDateParam(ddmmyyyy) {
  const [d, m, y] = ddmmyyyy.split("/");
  return `${y}/${m}/${d}`;
}

/**
 * Convert DD/MM/YYYY to YYYY-MM-DD for PostgreSQL date columns.
 */
export function toIsoDate(ddmmyyyy) {
  const [d, m, y] = ddmmyyyy.split("/");
  return `${y}-${m}-${d}`;
}

/**
 * Build a local race results URL.
 */
export function localResultsUrl(ddmmyyyy, racecourse, raceNo) {
  const datePart = toLocalDateParam(ddmmyyyy);
  return `${HKJC_BASE}/zh-hk/local/information/localresults?racedate=${datePart}&Racecourse=${racecourse}&RaceNo=${raceNo}`;
}

/**
 * Convert DD/MM/YYYY to YYYYMMDD for HKJC overseas URL query param.
 */
function toOverseasDateParam(ddmmyyyy) {
  const [d, m, y] = ddmmyyyy.split("/");
  return `${y}${m}${d}`;
}

/**
 * Build an overseas race results URL.
 */
export function overseasResultsUrl(ddmmyyyy, racecourse, raceNo) {
  const datePart = toOverseasDateParam(ddmmyyyy);
  return `${HKJC_BASE}/zh-hk/overseas/results?RaceDate=${datePart}&Racecourse=${racecourse}&RaceNo=${raceNo}`;
}

